package example.reporting.cucumberreport;

public class Tag {

    private String name;

    private long line;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getLine() {
        return line;
    }

    public void setLine(long line) {
        this.line = line;
    }

}
